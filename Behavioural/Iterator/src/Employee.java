public class Employee{

    private String name;
    private String empId;

    public void setName(String name)
    {
        this.name = name;
    }

    public void setEmpId(String empId)
    {
        this.empId = empId;
    }

    public String getName()
    {
        return this.name;
    }

    public String getEmpId()
    {
        return this.empId;
    }
}
